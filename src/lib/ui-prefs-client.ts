// Клиентское сохранение настроек интерфейса. Раскладка пулов — некритичные данные: сохраняем
// «вдогонку», сбой записи (сеть/сервер) молча игнорируем, чтобы не ломать взаимодействие. Источник
// истины при перезагрузке — сервер (страница грузит настройки заранее, без мигания раскладки).
import { apiSend } from "@/lib/fetcher";

export function persistUiPref(key: string, value: string[]): void {
  void apiSend("/api/ui-prefs", "PUT", { key, value }).catch(() => undefined);
}
