// Клиентское время действия для офлайн-режима водителя. Без сети водитель меняет статус/пишет
// комментарий, а запрос долетает до сервера позже (когда вернётся связь). В историю (TaskEvent.at)
// нужно писать момент НАЖАТИЯ, а не момент досылки — иначе хронология и KPI «невыполненная точка»
// считались бы по времени появления сети, а не по факту работы.
//
// Время приходит с клиента (заголовок X-Occurred-At, ISO), поэтому ему НЕЛЬЗЯ доверять на веру:
// телефонные часы можно перевести. Поэтому clamp в разумное окно — не в будущем и не глубже 36 часов
// (практический максимум офлайна за смену/сутки). Вне окна или мусор → берём время сервера.
// С O7 (решение Артёма 02.07.2026) смена тоже открывается/закрывается офлайн, её openedAt берётся
// отсюда же; контроль против подкрутки часов — пометка «открыта офлайн» Милене (Shift.openedOffline)
// и её право поправить время при подтверждении (№3), поверх этого clamp.

const MAX_PAST_MS = 36 * 60 * 60 * 1000; // 36 ч — глубже считаем недостоверным
const FUTURE_SKEW_MS = 2 * 60 * 1000; // 2 мин допуска на рассинхрон часов телефона

/**
 * Возвращает достоверное время события: клиентское `raw` (ISO), если оно в окне [now−36ч; now+2мин],
 * иначе — `now`. Чистая функция (now передаётся явно — удобно для тестов и детерминизма).
 */
export function resolveOccurredAt(raw: string | null | undefined, now: Date = new Date()): Date {
  if (!raw) return now;
  const t = new Date(raw);
  const ms = t.getTime();
  if (Number.isNaN(ms)) return now;
  const nowMs = now.getTime();
  if (ms > nowMs + FUTURE_SKEW_MS) return now; // будущее — не доверяем
  if (ms < nowMs - MAX_PAST_MS) return now; // слишком старое — не доверяем
  return t;
}
